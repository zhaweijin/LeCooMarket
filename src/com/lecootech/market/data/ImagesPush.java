package com.lecootech.market.data;

public class ImagesPush {

	private String name;
	private String swid;
	private String modifieddate;
	private String softwareBigIcon;
	private String sort;
	private String categoryid;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getSwid() {
		return swid;
	}
	public void setSwid(String swid) {
		this.swid = swid;
	}
	public String getModifieddate() {
		return modifieddate;
	}
	public void setModifieddate(String modifieddate) {
		this.modifieddate = modifieddate;
	}
	public String getSoftwareBigIcon() {
		return softwareBigIcon;
	}
	public void setSoftwareBigIcon(String softwareBigIcon) {
		this.softwareBigIcon = softwareBigIcon;
	}
	public String getSort() {
		return sort;
	}
	public void setSort(String sort) {
		this.sort = sort;
	}
	public String getCategoryid() {
		return categoryid;
	}
	public void setCategoryid(String categoryid) {
		this.categoryid = categoryid;
	}
}
